<h2>Description</h2>
  <h4>
    The application will have target to facilitate management of Foreign Exchange ("FX") and Money
    Market ("MM") transactions. The tool is linked with a MySQL database
    stored on a server that can be accessed online by a user. Main
    features include possibility to insert and modify financial
    transactions etc. It provides also for reporting of concluded
    deals. The application is scalable and planned to be enriched
    in time. The IT langages used in coding are HTML,
    CSS, JS with jQ, PHP (OOP) and SQL. 
  </h4>