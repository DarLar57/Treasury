<h2 id="glossary">Glossary</h2>
  <dl>
    <dt>Deposit</dt>
      <dd>
      Defined amount of money placed by a Company with a counterparty
      (e.g. Bank). For the simplicity the assumed base convention to
      calculate interest is act/365.
      </dd>
    <dt>FX Forward</dt>
      <dd>
        Forward deals are OTC (over-the-counter) bilateral transactions.
        They allow to buy or sell an underlying financial instrument
        (currency) at a price agreed today, but settlement of the deal is
        agreed to take place on a future date. Forwards are used for both
        trading and hedging purposes. Main factor impacting difference of
        exchange rate (at spot and forward date) is a difference in interest
        rates of the two involved currencies.
      </dd>
    <dt>FX Market (FX)</dt>
      <dd>
        Global decentralized or over-the-counter (OTC) market for the
        trading of currencies. This market determines foreign exchange rates
        for every currency. It includes all aspects of buying, selling and
        exchanging currencies at current or determined prices. In terms of
        trading volume, it is by far the largest market in the world
      </dd>
    <dt>FX SPOT</dt>
      <dd>
        Usually transactions that are agreed today, but the delivery /
        settlement and payment takes place up to two days time (mainly just
        2 days).
      </dd>
    <dt>FX Swap</dt>
      <dd>
        Short term (s/t) transaction involving two parties. It is an
        exchange of one currency for another currency between the two
        parties. This occurs usually upto two days time (spot date) and is
        at spot exchange rate. On the maturity date there is re-exchange of
        the currency amounts. This takes place at the forward rate that was
        also agreed at the start of the transaction. FX swaps are widely
        used to temporarily lend and borrow foreign currency and to manage
        liquidity of different currencies. Main factor impacting different
        exchange rate (at spot and forward date) is a difference in interest
        rates of the two involved currencies.
      </dd>
    <dt>Interest Rate</dt>
      <dd>
        Amount of interest due per period, as a proportion of the amount
        lent, deposited, or borrowed (called the principal sum). The total
        interest on an amount lent or borrowed depends on the principal sum,
        the interest rate, the compounding frequency, and the length of time
        over which it is lent, deposited, or borrowed.
      </dd>
    <dt>Interest</dt>
      <dd>
        Payment from a borrower or deposit-taking financial institution to a
        lender or depositor of an amount above repayment of the principal
        sum (that is, the amount borrowed), at a particular rate.
      </dd>
    <dt>Loan</dt>
      <dd>
        Lending of money by one or more individuals, organizations, or other
        entities to other individuals, organizations, etc. The recipient
        (i.e., the borrower) incurs a debt and is usually liable to pay
        interest on that debt until it is repaid as well as to repay the
        principal amount borrowed.
      </dd>
    <dt>Money Market (MM)</dt>
      <dd>
        Part of financial markets concerned with financial instruments that
        have a maturity shorter than one year eg wholesale deposits.
      </dd>
    <dt>Spread (Margin)</dt>
      <dd>
        Difference between yields on differing debt instruments calculated
        by deducting the yield of one instrument from the other. For loans
        it is most often expressed in basis points (bps) or percentage
        points p.a. as the difference of the all-in interest rate p.a. less
        a reference rate (e.g. WIBOR or EURIBOR).
      </dd>
